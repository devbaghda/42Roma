#include <unistd.h>
#include <stdio.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int	main(void)
{
	char	*str;

	str = "FANTASTICo";
	ft_putstr(str);
	return (0);
}
