#include <unistd.h>

void	loop(int n)
{
	char	c1;
	char	c2;
	char	c3;
	char	c4;

	c1 = '0' + (n - n % 1000) / 1000;
	c2 = '0' + (n % 1000 - n % 100) / 100;
	c3 = '0' + (n % 100 - n % 10) / 10;
	c4 = '0' + n % 10;
	write(1, &c1, 1);
	write(1, &c2, 1);
	write(1, " ", 1);
	write(1, &c3, 1);
	write(1, &c4, 1);
	if (n != 9899)
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb2(void)
{
	int	n;

	n = 0;
	while (n <= 9899)
	{
		if ((n - n % 100) / 100 >= n % 100)
		{
			n++;
		}
		else
		{
			loop(n);
			n++;
		}
	}
}
